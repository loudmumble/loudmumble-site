#!/usr/bin/env node
'use strict';
/*
 * gen-sa-fleet.js — generate lib/sa-fleet.ts from the loopd DERIVED fleet feed.
 *
 * Source of truth: the loopd pipeline
 *   seed-fleet-graph.js (owner roster) -> fleet-map.js (sanitized) -> fleet-derive-local.js (git facts)
 * which emits verify/fleet-map.portal.json. This script projects that feed into the public,
 * type-safe `SA_FLEET` the site renders. NO hand-curation: every field here comes from the feed.
 *
 * Run:  SA_FLEET_FEED=/path/to/fleet-map.portal.json node scripts/gen-sa-fleet.js
 *       (default: sibling ../loopd/verify/fleet-map.portal.json)
 * Node.js only.
 */
const fs = require('fs');
const path = require('path');

const FEED = process.env.SA_FLEET_FEED ||
  path.join(__dirname, '..', '..', 'loopd', 'verify', 'fleet-map.portal.json');
const OUT = path.join(__dirname, '..', 'lib', 'sa-fleet.ts');

// display metadata for the real `cat` taxonomy (faithful to the source categories)
const CAT = {
  offensive:     { label: 'Offensive',      color: '#ff5a52', order: 1 },
  defensive:     { label: 'Defensive',      color: '#4d9bff', order: 2 },
  infra:         { label: 'Infrastructure', color: '#34d3a6', order: 3 },
  sensor:        { label: 'Sensor',         color: '#88ddff', order: 4 },
  aiml:          { label: 'AI / ML',        color: '#aa66ff', order: 5 },
  orchestration: { label: 'Orchestration',  color: '#ffd23f', order: 6 },
  authority:     { label: 'Authority',      color: '#ff8c2e', order: 7 },
  agent:         { label: 'Agent',          color: '#22ccaa', order: 8 },
  surface:       { label: 'Surface',        color: '#66aaff', order: 9 },
  candidate:     { label: 'Candidate',      color: '#8a8a93', order: 10 },
};

if (!fs.existsSync(FEED)) {
  console.error(`gen-sa-fleet: feed not found at ${FEED}\n  set SA_FLEET_FEED or run the loopd pipeline first.`);
  process.exit(1);
}
const feed = JSON.parse(fs.readFileSync(FEED, 'utf8'));

const nodes = feed.nodes.map((n) => ({
  id: n.id,
  label: n.label,
  cat: n.cat,
  tagline: n.tagline || '',
  present: n.present !== false,
  version: n.version ?? null,
  status: n.status || '',
  commits_since_tag: n.commits_since_tag ?? null,
  last_commit: n.last_commit ?? null,
  mcp: n.mcp === true,
  awaiting: (n.planes || []).includes('awaiting'),
  planes: n.planes || [],
}));

// cats present in the data, in taxonomy order, with live counts
const cats = Object.keys(CAT)
  .filter((id) => nodes.some((n) => n.cat === id))
  .sort((a, b) => CAT[a].order - CAT[b].order)
  .map((id) => ({ id, label: CAT[id].label, color: CAT[id].color, count: nodes.filter((n) => n.cat === id).length }));

const fleet = {
  generated: feed.generated || new Date().toISOString().slice(0, 10),
  derived_at: feed.derived_at || null,
  stats: {
    nodes: nodes.length,
    present: nodes.filter((n) => n.present).length,
    tagged: nodes.filter((n) => n.version).length,
    mcp: nodes.filter((n) => n.mcp).length,
    awaiting: nodes.filter((n) => n.awaiting).length,
    edges: (feed.connections || []).length,
  },
  cats,
  nodes,
};

// fail-closed: the public feed must never carry infra/PII
const LEAK = /192\.168\.50\.|spiffe:\/\/|gitlab\.loudmumble|\/home\/|jasonericterry@/;
const body = JSON.stringify(fleet, null, 2);
if (LEAK.test(body)) { console.error('gen-sa-fleet: ABORT — leak detected, not writing.'); process.exit(2); }

const ts = `// AUTO-GENERATED by scripts/gen-sa-fleet.js from the loopd derived fleet feed. DO NOT EDIT BY HAND.
// Regenerate: node scripts/gen-sa-fleet.js   (source: loopd seed-fleet-graph -> fleet-map -> fleet-derive-local)
// Every value below is roster (owner-declared) + facts derived from the real git repos — never hand-typed.
export interface SANode { id: string; label: string; cat: string; tagline: string; present: boolean; version: string | null; status: string; commits_since_tag: number | null; last_commit: string | null; mcp: boolean; awaiting: boolean; planes: string[]; }
export interface SACat { id: string; label: string; color: string; count: number; }
export interface SAFleet { generated: string; derived_at: string | null; stats: { nodes: number; present: number; tagged: number; mcp: number; awaiting: number; edges: number }; cats: SACat[]; nodes: SANode[]; }

export const SA_FLEET: SAFleet = ${body};
`;

fs.writeFileSync(OUT, ts);
console.log(`gen-sa-fleet: ${fleet.stats.nodes} nodes (${fleet.stats.present} present, ${fleet.stats.tagged} tagged) across ${cats.length} categories -> lib/sa-fleet.ts`);
console.log('  cats: ' + cats.map((c) => `${c.label}=${c.count}`).join(' · '));
